# Исходный двумерный массив
data = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9],
]

# Дополнительная колонка для добавления
new_column = [10, 20, 30]

# Добавление новой колонки в каждую строку массива
for i in range(len(data)):
    data[i].append(new_column[i])

# Вывод результата
for row in data:
    print(row)
